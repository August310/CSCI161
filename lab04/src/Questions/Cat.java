public class Cat {

    private String catName;
    private int miceCaught;
    private boolean secretlyPlottingToKillYou;

    public Cat() {
        catName = "Hermy";
        miceCaught = 0;
        secretlyPlottingToKillYou = false;
    }

    public Cat(String catName, int miceCaught, boolean secretlyPlottingToKillYou) {
        this.catName = catName;
        this.miceCaught = miceCaught;
        this.secretlyPlottingToKillYou = secretlyPlottingToKillYou;
    }

    public String getCatName() {
        return catName;
    }

    public int getMiceCaught() {
        return miceCaught;
    }

    public boolean getSecretlyPlottingToKillYou() {
        return secretlyPlottingToKillYou;
    }

    public void setCatName(String newCatName) {
        catName = newCatName;
    }

    public void setMiceCaught(int numberMiceCaught) {
        miceCaught = numberMiceCaught;
    }

    public void setSecretlyPlottingToKillYou(boolean isSecretlyPlottingToKillYou) {
        secretlyPlottingToKillYou = isSecretlyPlottingToKillYou;
    }

    public String toString() {
        return "My cat's name is " + catName + ". They have caught " + miceCaught + " mice. My cat stated it is " + secretlyPlottingToKillYou + " that they're secretly plotting to kill me.";
    }

}
